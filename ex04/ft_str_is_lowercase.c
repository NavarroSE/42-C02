int	ft_str_is_lowercase(char *str)
{
	int	n;
	int	c;

	n = 1;
	c = 0;
	if (str[0] == '\0')
		n = 1;
	else
	{
		while (str[c] != '\0' && n != 0)
		{
			if ((str[c] >= 'a' && str[c] <= 'z'))
				n = 1;
			else
				n = 0;
			c++;
		}
	}
	return (n);
}
